# estrutura de controle = laços
for c in range(0,2):
    print("Olá mundo.") # lembrar sempre da identação correta
for c in range(0,6,2): # significa que vai contar da posição 0 até 6 pulando de 2 em 2
    print("Olá mundo!")
for c in range(6,0,-1):
    print("Olá mundo?")
n=int(input("Digite um número:"))
for c in range(0,n):
    print("Olá mundo.")